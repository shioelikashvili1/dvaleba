// 1. ტოლობის შემოწმება (==)
console.log(5 == "5"); // true (ტოლობის ოპერატორი არ ამოწმებს ტიპს)

// 2. ტოლობის შემოწმება ტიპის გათვალისწინებით (===)
console.log(5 === "5"); // false (ტიპი განსხვავებულია)

// 3. უთანასწორობის შემოწმება (!=)
console.log(5 != "5"); // false (ოპერატორი არ ამოწმებს ტიპს)

// 4. უთანასწორობა ტიპის გათვალისწინებით (!==)
console.log(5 !== "5"); // true (ტიპები განსხვავებულია)

// 5. ნაკლებობის შემოწმება (<)
console.log(3 < 5); // true

// 6. მეტობის შემოწმება (>)
console.log(10 > 5); // true

// 7. ნაკლებობის ან ტოლობის შემოწმება (<=)
console.log(5 <= 5); // true

// 8. მეტობის ან ტოლობის შემოწმება (>=)
console.log(7 >= 10); // false

// 9. მნიშვნელობების შედარება
const a = 10, b = 20;
console.log(a < b); // true

// 10. სტრიქონების შედარება
console.log("apple" < "banana"); // true (ანბანური შედარება)


// 1. პოსტფიქსური ინკრემენტი
let x = 5;
console.log(x++); // 5 (ამოწერს მნიშვნელობას და შემდეგ გაზრდის)
console.log(x);   // 6

// 2. პოსტფიქსური დეკრემენტი
let y = 10;
console.log(y--); // 10 (ამოწერს მნიშვნელობას და შემდეგ შემცირებს)
console.log(y);   // 9

// 3. ცვლადის გამოყენება პოსტფიქსურით
let a = 3;
let b = a++;
console.log(a); // 4
console.log(b); // 3 (ძველი მნიშვნელობა)

// 4. ჩადგმული ლოგიკური ოპერაცია
let p = 7;
console.log(p++ > 6); // true (შედარებას წინ უძღვის გაზრდა)

// 5. მასივში გამოყენება
let arr = [0, 1, 2];
let i = 0;
console.log(arr[i++]); // 0 (i ახლა 1 გახდა)
console.log(arr[i]);   // 1


